public class Date{
  public static void main(String[] args){
    //initializing variables
    String day = "Thursday";
    int date = 16;
    String month = "July";
    int year = 2015;
    //American format display
    System.out.println("American format:");
    System.out.print(day);
    System.out.print(", ");
    System.out.print(month);
    System.out.print(" ");
    System.out.print(date);
    System.out.print(", ");
    System.out.println(year);
    //European format display
    System.out.println("European format:");
    System.out.print(day);
    System.out.print(" ");
    System.out.print(date);
    System.out.print(" ");
    System.out.print(month);
    System.out.print(" ");
    System.out.println(year);
  }
}
